    private void jBtnCreateAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCreateAccountActionPerformed
        String username= jUsername.getText();
        String email= jEmail.getText();
        String password= jPassword.getText();
        String confirmPassword= jConfirmPassword.getText();
        
        if(username.equals("")&&email.equals("")&&password.equals("")&&confirmPassword.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter the details", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if(username.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter username", "Error",JOptionPane.ERROR_MESSAGE);
        }
        else if(email.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter the email", "Error",JOptionPane.ERROR_MESSAGE);
        }
        else if(password.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter the password", "Error",JOptionPane.ERROR_MESSAGE);
        }
        else if(confirmPassword.equals("")){
            JOptionPane.showMessageDialog(null, "Please confirm password", "Error",JOptionPane.ERROR_MESSAGE);
          
        }
        else if(password == null ? confirmPassword != null : !password.equals(confirmPassword)){
            JOptionPane.showMessageDialog(null, "Password do not match", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            PreparedStatement ps;
            ResultSet rs;
            Boolean checkUser= false;
            String query= "SELECT * FROM users WHERE username=?";
            try{
            ps=myConnection.getConnection().prepareStatement(query);
            ps.setString(1, username);
              rs=ps.executeQuery();
              if(rs.next()){
                checkUser=true;
            }
            } 
            catch(SQLException ex){
               Logger.getLogger(NewAccount.class.getName()).log(Level.SEVERE,null,ex);
            }
            if(checkUser==true){
                JOptionPane.showMessageDialog(null, "Username taken", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else{
                String query2="INSERT INTO users(username,email,password)VALUES(?,?,?)";
                try{
                    ps=myConnection.getConnection().prepareStatement(query2);
                    ps.setString(1, username);
                    ps.setString(2, email);
                    ps.setString(3, password);
                   if(ps.executeUpdate()>0){
                       JOptionPane.showMessageDialog(null,"Successfully registered","Success",JOptionPane.INFORMATION_MESSAGE);
                   }
                   else{
                       JOptionPane.showMessageDialog(null, "Could not register", "Error", JOptionPane.ERROR_MESSAGE);
                   }
               
                }
                catch(SQLException ex){
                    Logger.getLogger(NewAccount.class.getName()).log(Level.SEVERE,null,ex);
                }
            }
        }
    }//GEN-LAST:event_jBtnCreateAccountActionPerformed

