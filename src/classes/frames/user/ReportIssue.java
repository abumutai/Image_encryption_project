    private void JbtnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbtnLoginActionPerformed
        String username=nm.getText();
        String subject= jSubject.getText();
        String description= jDescription.getText();
        java.sql.Timestamp date = new java.sql.Timestamp(new java.util.Date().getTime());
        if(subject.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter the subject","ERROR",JOptionPane.WARNING_MESSAGE);
        }
        else if(description.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter the description","ERROR",JOptionPane.ERROR_MESSAGE);
        }
        else{

            PreparedStatement ps;
            ResultSet rs;
            String query="INSERT INTO issues(username,subject,issues,date) VALUES(?,?,?,?)";
            try {
                ps= myConnection.getConnection().prepareStatement(query);
                ps.setString(1, username);
                ps.setString(2, subject);
                ps.setString(3,description);
                ps.setTimestamp(4,date);
                if(ps.executeUpdate()>0){
                JOptionPane.showMessageDialog(null,"Issue reported successfully","SUCCESS",JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(null,"Could not report issue","ERROR",JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(ReportIssue.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_JbtnLoginActionPerformed

